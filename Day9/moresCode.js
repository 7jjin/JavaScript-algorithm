function solution(letter) {
    morse = { 
      '.-':'a','-...':'b','-.-.':'c','-..':'d','.':'e','..-.':'f',
      '--.':'g','....':'h','..':'i','.---':'j','-.-':'k','.-..':'l',
      '--':'m','-.':'n','---':'o','.--.':'p','--.-':'q','.-.':'r',
      '...':'s','-':'t','..-':'u','...-':'v','.--':'w','-..-':'x',
      '-.--':'y','--..':'z'
  }   
      return letter.split(' ').map(a=>morse[a]).join('');   // 입력받은 문자열을 공백 단위로 자른 후, 새로운 배열을 만든다.

  }